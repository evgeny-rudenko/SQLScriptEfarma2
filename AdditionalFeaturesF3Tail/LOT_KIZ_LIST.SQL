SELECT 
L.ID_LOT,
KIZ.GTIN,
KIZ.GTIN_SGTIN
FROM LOT L
INNER JOIN STORE ST ON ST.ID_STORE=L.ID_STORE
INNER JOIN KIZ_2_DOCUMENT_ITEM KDI ON KDI.ID_DOCUMENT_ITEM_ADD=L.ID_DOCUMENT_ITEM_ADD
INNER JOIN KIZ ON KIZ.ID_KIZ_GLOBAL=KDI.ID_KIZ_GLOBAL
WHERE 1=1
AND L.QUANTITY_REM > 0
and KDI.REMAIN>0
AND KIZ.STATE<>'EXIT'
--and st.ID_STORE=2
and ST.ID_CONTRACTOR = DBO.FN_CONST_CONTRACTOR_SELF()
--AND L.ID_LOT=223858
--AND GTIN_SGTIN<>''
ORDER BY L.ID_LOT


